def save_plot(name, title, interpretation, data_table=None):
    plt.title(title, fontsize=15, pad=20)
    path = f"{IMAGE_DIR}/{name}.png"
    plt.savefig(path, bbox_inches='tight', dpi=300)
    plt.close()
    write_to_report(f"### {title}")
    write_to_report(f"![{title}](./{path})")
    write_to_report(f"\n**분석 및 비즈니스 인사이트 (200자 이상):**\n{interpretation}\n")
    if data_table is not None:
        write_to_report("**[통계 데이터 요약]**")
        write_to_report(data_table.to_markdown() + "\n")

